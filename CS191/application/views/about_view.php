<font style="font-size:25px;" style='color: #597F0B; text-decoration : underline; font-family: Lucida Sans Unicode", "Lucida Grande", Sans-Serif;'><strong> UP DCS Research Submission Portal </strong><br/><br/></font>


The Research Submission Portal will allow faculty and students of the UP Department of
Computer Science to submit their research proposals online. And also the review committee members
can also give their verdicts online too, so as to allow real time submission and results. <br/><br/>

Not only does it provide easy acces to research proposal submissions, it also provides awareness to 
the public on what's happening in the Department of Computer Science. <br/><br/>

In line with the department’s vision to <strong>“…be a world class research and teaching institution in
the field of Computer Science that serves the interests of the country”</strong> and its mission to <strong>“…
advance the discipline of computing through internationally recognized research and
development…”</strong> and the University of the Philippines College of Engineering’s vision to be <strong>“A
global leader in engineering, education, research, and technology innovation”</strong>, the project
paves the way for access to research proposal submissions and decisions in a fast, easy, and
convenient way. Knowing that it is research that fuels up science and development in a country,
it is very important for research to flourish and develop. Also public awareness is a main key in
national development.<br/><br/>

